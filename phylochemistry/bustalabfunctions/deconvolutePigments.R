#### Shiny port pool for jupyter = TRUE
####
#### Same 13 nginx-proxied ports as analyzeGCMSdata4.R / phylochemistry.R, but
#### with PIGMENT-namespaced helpers so sourcing both files in one session does
#### not clobber the GCMS app's pick_shiny_port(). Picking binds the port, so the
#### two apps never collide on a port. If you change the pool, keep all copies in
#### sync (see bustalabfunctions/AGENTS.md).

    .pigment_shiny_port_pool <- data.frame(
        port      = c(10101:10110, 10123, 10456, 10789),
        subdomain = c(paste0("shiny", 1:10), "shiny123", "shiny456", "shiny789"),
        stringsAsFactors = FALSE
    )

    .pigment_port_is_free <- function(port) {
        tryCatch({
            s <- httpuv::startServer("127.0.0.1", port,
                                     list(call = function(req) list(status = 200, body = "")))
            s$stop()
            TRUE
        }, error = function(e) FALSE)
    }

    .pigment_pick_shiny_port <- function() {
        for (i in sample(nrow(.pigment_shiny_port_pool))) {
            p <- .pigment_shiny_port_pool$port[i]
            if (.pigment_port_is_free(p)) {
                return(list(
                    port = p,
                    url  = paste0("https://", .pigment_shiny_port_pool$subdomain[i], ".bustalab.d.umn.edu")
                ))
            }
        }
        NULL
    }

